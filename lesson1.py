#підклюення модуля
import turtle
t = turtle.Pen()
t.color(1,0,1)
t.begin_fill()
t.forward(200)
t.left(90)
t.forward(200)
t.left(90)
t.forward(200)
t.left(90)
t.forward(200)
t.end_fill()
t.reset()
t.color(1,1,0)
t.backward(200)
t.up()
t.right(90)
t.forward(20)
t.left(90)
t.down()
t.forward(200)
t.color(1,0,0)
t.begin_fill()
t.circle(80)
t.end_fill()
